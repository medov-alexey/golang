package main

import "fmt"

func main() {
        fmt.Println("\nСимвол и его кодовое обозначение в таблице кодировок Unicode:\n")
	fmt.Println("A -",'A')
	fmt.Println("B -",'B')
	fmt.Println("Ж -",'Ж')
	fmt.Println("\\t -",'\t',"(табуляция)")
	fmt.Println("\\n -",'\n',"(переход на новую строку)")
	fmt.Println("\\ -",'\\',"(обратный слэш)")
}
